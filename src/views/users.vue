<script setup>
import { onMounted, ref, watch } from "vue";
import { FireStore } from "@/utils";
import Layout from "@/layout/admin.vue";
import Avatar from "@/components/avatar.vue";
import Breadcrumb from "@/components/breadcrumb.vue"

const data = ref([]);
const users = ref([]);
const search = ref("");
const loading = ref(true);
const db = new FireStore();

function filterUser(phrase) {
  const nameOrEmail = phrase.trim();
  const filterUsers = data.value.filter(
    (user) => user.name.search(nameOrEmail) !== -1 || user.email.search(nameOrEmail) !== -1
  );
  users.value = filterUsers;
}

function deleteUser(userId) {
  const filterUsers = data.value.filter((user) => user.id !== userId);
  users.value = data.value = filterUsers;
}

async function fetchUsers() {
  const records = await db.getRecords("users");
  data.value = records;
  users.value = records;
  loading.value = false;
}

onMounted(() => {
  fetchUsers();
});

watch(search, (value) => {
  filterUser(value);
});
</script>